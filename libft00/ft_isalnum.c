#include <stdio.h>

int	ft_isalnum(int c)
{
	return ( '0' <= c && c <= '9' || 'A' <= c && c <= 'Z' || 'a' <= c && c <= 'z');
}

int	main(void)
{
	char	c = '!';

	printf("%d", ft_isalnum(c));
	return (0);
}
